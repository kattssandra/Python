var_1 = 37
var_2 = 99

print("Исходные значения переменных:")
print("var_1 =", var_1)
print("var_2 =", var_2)

# Обмен значениями переменных с использованием временной переменной
temp_var = var_1
var_1 = var_2
var_2 = temp_var

# Вывод обновленных значений переменных
print("Значения переменных после обмена:")
print("var_1 =", var_1)
print("var_2 =", var_2)
